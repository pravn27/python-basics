# Addition, Subtraction, Multiplication & Division
add = 2 + 3
sub = 5 - 4
mul = 6 * 2
print(add, sub, mul)
# Division always return in float value
div = 5 / 2
print(div)
# Integer/Floor division
print( 5 // 2, 4 // 5 )
# Multiple operation together - using BODMAS rule
print(5+3-10*2)
print((5+3) * 2)
# Power operator **
print(2 ** 3)
print(2 ** 4)
# Module operator %, which returns remainder
print(5 % 2)